num = int(input("Введите номер места: "))
if num == 0 or num > 53:
    print("Введите корректный номер места( 1 - 53)")
if 1 <= num <= 36:
    if num % 2 == 0:
        print("Ваше место: верхнее")
    else:
        print("Ваше место: нижнее")
if 37 <= num <= 53:
    if num % 2 == 0:
        print("Ваше место: боковое верхнее")
    else:
        print("Ваше место: боковое нижнее")